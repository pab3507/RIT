              <h3>Bachata</h3>
              <p>In the other hand, Bachata, a genre of music and dance that origins reside in the rural and marginal
								 neighborhoods of the country, is characterized by its coordinated movements and more sensual steps.
								 Originally referred to as “amargue”, meaning bitterness, until the more neutral but ambiguous term
								 bachata became popular. Bachata has been thoroughly influenced by several elements of Latin American
								 Guitar styles as well as bolero and merengue. Bachata has become extremely popular due to the global
								 exposure that some artists have given it such as: Anthony Santos, Zacharias Ferreira, Joe Veras,
								 Raulín Rodríguez, Frank Reyes, Fernando Villalona, Hector Acosta, Aventura, etc. Currently,
								 Prince Royce and Romeo Santos are the two most active and popular artists forming part of this
								 musical genre.</p>
