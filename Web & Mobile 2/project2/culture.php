              <h3>Cuisine</h3>
              <p>Dominican cuisine is influenced mostly by the Spanish, Taíno, and African elements of our culture. The
								 typical cuisine resembles other caribbean dishes. The main lunch dish is called “La Bandera” consists of
								 a type of meat, rice and beans. The traditional breakfast dish is called “Mangú”, a name originating from
								 United States interventions where the navy were introduced this dish and they always exclaimed “Man,
								 Good!” and thus this named catched on for this dish. The dish consists of sunnyside eggs, mangú (mashed,
								 boiled plantains) topped by onions, deep-fried meat (Dominican salami, typically), and fried cheese.
								 Mofongo is another popular dish made by deep fried mashed plantains along with several seasonings and
								 regularly accompanied by meat. Sancocho is another traditional dish, consisting of stew often made with
								 several varieties of meat. </p>
							<p>Other favorite Dominican foods include chicharrón, yuca, casabe, pastelitos (empanadas), batata, yam,
								 pasteles en hoja, chimichurris, and tostones. Popular sweets include: arroz con leche, habichuelas con
								 dulce, flan, dulce de leche, etc. The famous dominican beverages are: Morir Soñando, Ron Brugal and Ron
								 Barceló, Presidente Beer, Mama Juana, mabí, etc.</p>
